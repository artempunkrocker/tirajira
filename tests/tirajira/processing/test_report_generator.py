@patch("tirajira.processing.report_generator.get_logger")
@patch("tirajira.processing.report_generator.create_report_writer")
@patch("tirajira.processing.report_generator.datetime")
def test_generate_report_with_issue_urls(
    mock_datetime, mock_create_report_writer, mock_get_logger
):
    """Test: report generation with adding Jira task URLs."""
    # Mock logger so it doesn't output to console
    mock_logger_instance = Mock()
    mock_get_logger.return_value = mock_logger_instance

    # Setup mock time
    fixed_time = datetime(2023, 12, 1, 12, 0, 0)
    mock_datetime.now.return_value = fixed_time

    # Create mocks
    mock_writer = Mock()
    mock_create_report_writer.return_value = mock_writer

    # Create report generator
    report_generator = ReportGenerator()

    # Data for report with tasks having keys
    processing_details = [
        {
            "id": 0,
            "status": "success",
            "issue_key": "PROJ-123",
        },
        {
            "id": 1,
            "status": "success",
            "issue_key": "PROJ-124",
        },
        {
            "id": 2,
            "status": "failed",
            # No issue_key for failed task
        },
    ]

    # Call the report generation method with Jira server specified
    report_generator.generate_report(
        source_file="tasks.json",
        processing_details=processing_details,
        report_file="report_with_urls.json",
        successful_count=2,
        total_count=3,
        jira_server="https://example.atlassian.net",
    )

    # Check that writer.write_report was called
    mock_writer.write_report.assert_called_once()

    # Check the report content
    call_args = mock_writer.write_report.call_args[0]
    report_data = call_args[0]
    tasks = report_data["tasks"]

    # Check that task URLs were added
    assert len(tasks) == 3
    assert tasks[0]["issue_key"] == "PROJ-123"
    assert tasks[0]["issue_url"] == "https://example.atlassian.net/browse/PROJ-123"
    assert tasks[1]["issue_key"] == "PROJ-124"
    assert tasks[1]["issue_url"] == "https://example.atlassian.net/browse/PROJ-124"

    # Check that URLs are added only for tasks with keys
    assert "issue_url" in tasks[0]
    assert "issue_url" in tasks[1]

    # Check that URL is not added for task without key
    assert "issue_key" not in tasks[2]
    assert "issue_url" not in tasks[2]


