@patch("tirajira.processing.report_generator.get_logger")
@patch("tirajira.processing.report_generator.create_report_writer")
@patch("tirajira.processing.report_generator.datetime")
def test_generate_report_auto_filename(
    mock_datetime, mock_create_report_writer, mock_get_logger
):
    """Test: automatic report file name generation."""
    # Mock logger so it doesn't output to console
    mock_logger_instance = Mock()
    mock_get_logger.return_value = mock_logger_instance

    # Setup mock time
    fixed_time = datetime(2023, 12, 1, 12, 0, 0)
    mock_datetime.now.return_value = fixed_time

    # Create mocks
    mock_writer = Mock()
    mock_create_report_writer.return_value = mock_writer

    # Create report generator
    report_generator = ReportGenerator()

    # Data for report
    processing_details = [
        {
            "id": 0,
            "status": "success",
            "issue_key": "PROJ-123",
        },
    ]

    # Call the report generation method with automatic file name
    report_generator.generate_report(
        source_file="tasks.json",
        processing_details=processing_details,
        report_file=True,  # Automatic file name
        successful_count=1,
        total_count=1,
    )

    # Check that create_report_writer was called with the correct argument
    mock_create_report_writer.assert_called_once_with("json")

    # Check that writer.write_report was called with the correct file path
    call_args = mock_writer.write_report.call_args[0]
    report_data = call_args[0]
    file_path = call_args[1]

    assert file_path == "tirajira_report_20231201_120000.json"
    assert report_data["metadata"]["source_file"] == "tasks.json"
    assert report_data["metadata"]["total_tasks"] == 1
    assert report_data["metadata"]["successful_tasks"] == 1
    assert report_data["metadata"]["failed_tasks"] == 0
    assert len(report_data["tasks"]) == 1
    assert report_data["tasks"][0]["issue_key"] == "PROJ-123"

    # Check that the generated_at field contains the correct timestamp
    # We can't check the exact value since it's generated by datetime.now().isoformat()
    assert "generated_at" in report_data["metadata"]


