    """Test: exception handling during report generation."""
    # Create mocks
    mock_logger = Mock()
    mock_get_logger.return_value = mock_logger
    mock_create_report_writer.side_effect = Exception("Error creating report")

    # Create report generator
    report_generator = ReportGenerator()

    # Data for report
    processing_details = [
        {
            "id": 0,
            "status": "success",
            "issue_key": "PROJ-123",
        },
    ]

    # Call the report generation method
    report_generator.generate_report(
        source_file="tasks.json",
        processing_details=processing_details,
        report_file="report.json",
        successful_count=1,
        total_count=1,
    )

    # Check that the logger recorded the error
    mock_logger.error.assert_called_once()
